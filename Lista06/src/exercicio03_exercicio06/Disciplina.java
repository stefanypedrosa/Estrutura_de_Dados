package exercicio03_exercicio06;

public class Disciplina {
	int idDisc;
	String nome;
	
	public Disciplina(int idDisc, String nome) {
		this.idDisc = idDisc;
		this.nome = nome;
	}

	public int getIdDisc() {
		return idDisc;
	}

	public void setIdDisc(int idDisc) {
		this.idDisc = idDisc;
	}

	public String getNome() {
		return nome;
	}

	public void setNome(String nome) {
		this.nome = nome;
	}
	
	
}
